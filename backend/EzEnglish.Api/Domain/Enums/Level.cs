namespace EzEnglish.Api.Domain.Enums;

/// <summary>
/// CEFR-aligned learner levels, mapped to age groups in ADR-0008.
/// </summary>
public enum Level
{
    PreA1 = 1, // ages 3–4, pre-reader
    A0    = 2, // ages 5–6, early learner
    A1    = 3, // ages 7–8, beginner
    A2    = 4, // ages 9–10, elementary
    B1    = 5, // ages 11–12, pre-intermediate
}
